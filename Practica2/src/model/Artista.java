package model;

public class Artista {
    private String nom, id, tipus, idSerie, nacionalitat;

    public Artista(String id, String nom, String tipus, String idSerie, String nacionalitat){
        this.nom = nom;
        this.id = id;
        this.tipus = tipus;
        this.idSerie = idSerie;
        this.nacionalitat = nacionalitat;
    }

}
