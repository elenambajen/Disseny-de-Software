package model;

public class Subscripcio {
    private String id, client, serie, numTemporada, episodi;


    public Subscripcio(String id, String client, String serie, String numTemporada, String episodi) {
        this.client = client;
        this.episodi = episodi;
        this.id = id;
        this.serie = serie;
        this.numTemporada = numTemporada;
    }


}
